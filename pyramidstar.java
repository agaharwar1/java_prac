class pyramidstar {

    public static void main(String[] args) {

        for(int a=5;a>0;a--){
            int b=0;
            for(b=0;b<a;b++){
                System.out.print(" ");
                
            }
            for (int j=b;j<5;j++){
                System.out.print(" * ");
            }
            System.out.println("");

        }
    }
}
